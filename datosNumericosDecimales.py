# TIPOS DE VARIABLE EN PYTHON
# el tipo de variable estará determinado por el tipo de dato que almacena 


# Decimales: agregar un número con punto flotante 
a = 10.0
b = 12.5
c = 3.1416

# Mostrar los valores y tipo de datos 
print("valor de a:",a,"| tipo de dato de a:",type(a))
print("valor de b:",b,"| tipo de dato de b:",type(b))
print("valor de c:",c,"| tipo de dato de c:",type(c))
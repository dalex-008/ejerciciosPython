# Booleanos: Datos que pueden tomar verdadero o falso
# Se pueden usar las palabras reservadas: True y False 

# Definicion de variables booleanas 
verdad = True 
print(verdad,type(verdad))
falso = False
print(falso,type(falso))

# Mostrar resultados y tipos de datos 
print("El valor de verdad es:",verdad)
print("El valor de falso es:",falso)

# Utilizando datos booleanos en una evaluacion 
mayor_que_cero = 5>0
menor_que_cero = -3<0

# Mostrar resultados de evaluaciones 
print("¿Es 5 mayor que 0?",mayor_que_cero)
print("¿Es -3 menor que cero?",menor_que_cero)
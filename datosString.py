# TIPOS DE VARIABLE EN PYTHON 
# El tipo de variabe estara determinado por el tipo de dato que almacena 

# Texto o Cadena o String 
saludo = "hola"
color = "rojo"

# Tambien se puede usar comillas simples 
saludo_simple = "Buenos dias"
color_simple = "Azul"

# Mostrar los valores y tipo de datos 
print("valor de saludo:",saludo,"| tipo de dato de saludo:",type(saludo))
print("valor de color:",color,"| tipo de dato de color:",type(color))
print("valor de saludo_simple:",saludo_simple,"| tipo de dato de saludo_simple:",type(saludo_simple))
print("valor de color_simple:",color_simple,"| tipo de dato de color_simple:",type(color_simple))